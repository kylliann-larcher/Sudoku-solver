def print_grid_1(grid):


    print("     A   B   C    D   E   F    G   H   I")
    print("  ╔═══╤═══╤═══╦═══╤═══╤═══╦═══╤═══╤═══╗")
    
    for i in range(9):
        print(f"{i+1} ║", end="")
        
        for j in range(9):
            num = grid[i][j]
            content = '.' if num == '_' else str(num)
            print(f" {content} ", end="")
            
            if j in (2, 5):
                print("║", end="")
            elif j != 8:
                print("│", end="")
        
        print("║")
        
        if i in (2, 5):
            print("  ╠═══╪═══╪═══╬═══╪═══╪═══╬═══╪═══╪═══╣")
        elif i != 8:
            print("  ╟───┼───┼───╫───┼───┼───╫───┼───┼───╢")
    
    print("  ╚═══╧═══╧═══╩═══╧═══╧═══╩═══╧═══╧═══╝")

